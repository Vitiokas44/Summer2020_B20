package day03_sequences_Variables;

public class Escape_Sequences {
    public static void main(String[] args) {
        System.out.println("HELLO everyone My name is  CYBERTEK");
        System.out.print("\tHELLO everyone\n My name is  CYBERTEK");
        System.out.println("\\\\"); // will print 2 backslash
        System.out.println("\\"); //print one backslash
        System.out.println("//");
        System.out.println("/");
        System.out.println("My name is \' victor\' ");

    }


}
