package day20while_Loops;

public class whileLoopPractice {
    public static void main(String[] args) {
        /*print 1to 10 convert to while loop*/
        for (int i = 1; i <= 10; i++) {
            System.out.print(i+" ");
        }
        System.out.println();


        int num = 1;
        while(num<=10){
            System.out.print(num+" ");
            num++;
        }







    }}