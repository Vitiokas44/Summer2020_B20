package day12_Scanner_switch;

public class switch_ {
    public static void main(String[] args) {
        int a =1;
        switch (a){
            case 5:
                System.out.println("five");
           // break;
            case 1:
                System.out.println("one");
             break;
            default:
                System.out.println("Invalid Cases");
        }
    }


}
