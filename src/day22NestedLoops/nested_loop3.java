package day22NestedLoops;

public class nested_loop3 {
    public static void main(String[] args) {

        /*
         *******
         *******
         *******
         *******
         */


        int j = 1;
        while(j<= 4){

            for(int i = 1; i <= 10; i++){
                System.out.print("*");
            }
            System.out.println();

            j++;
        }





    }

}